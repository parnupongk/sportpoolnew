package com.isport.sportpool.data;

public class DataElementAnalyseHeadToHead
{
	public String totalGa = "";
	public String totalGs = "";
	public String totalLoss = "";
	public String totalDraw = "";
	public String totalWin = "";
	public String totalMatch = "";
	public String name = "";
	
	public DataElementAnalyseHeadToHead(String totalGa, String totalGs, String totalLoss, String totalDraw,
			String totalWin, String totalMatch, String name)
	{
		this.totalGa = totalGa;
		this.totalGs = totalGs;
		this.totalLoss = totalLoss;
		this.totalDraw = totalDraw;
		this.totalWin = totalWin;
		this.totalMatch = totalMatch;
		this.name = name;
	}
}
